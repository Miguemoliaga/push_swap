#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	char	*strend;

	strend = (char *)str + ft_strlen((char *)str);
	while (str <= strend)
	{
		if (*strend == (char)c)
			return (strend);
		strend--;
	}
	return (NULL);
}
