#include "libft.h"

void	ft_bzero(void *str, size_t n)
{
	size_t				i;
	unsigned char		*strb;

	strb = (unsigned char *)str;
	i = 0;
	while (i < n)
	{
		*strb = 0;
		strb++;
		i++;
	}
}
