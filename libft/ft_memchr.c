#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*sc;

	sc = (unsigned char *)s;
	while (n > 0)
	{
		if (*sc == (unsigned char)c)
			return (sc);
		n--;
		sc++;
	}
	return (NULL);
}
