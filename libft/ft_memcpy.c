#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t		i;
	char		*strdest;
	char		*strsrc;

	if (!dest && !src)
		return (NULL);
	i = 0;
	strdest = (char *)dest;
	strsrc = (char *)src;
	while (i < n)
	{
		strdest[i] = strsrc[i];
		i++;
	}
	return (strdest);
}
