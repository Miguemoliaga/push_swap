#include "libft.h"

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	if (!*little)
		return ((char *)big);
	if (len > ft_strlen(big))
		len = ft_strlen(big);
	while (i < len)
	{
		if (big[i + j] == little[j] && i + j < len)
		{
			j++;
			if (!little[j])
				return ((char *)big + i);
		}
		else
		{
			i++;
			j = 0;
		}	
	}
	return (NULL);
}
