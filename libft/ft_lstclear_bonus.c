#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*current;
	t_list	*aux;

	if (lst && del)
	{
		current = *lst;
		while (current)
		{
			(del)(current->content);
			aux = current->next;
			free(current);
			current = aux;
		}
		*lst = NULL;
	}
}
