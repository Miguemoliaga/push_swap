#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int		i;
	char	*strc;

	strc = (char *)str;
	i = ft_strlen(strc) + 1;
	while (i--)
	{
		if (*(unsigned char *)strc == (unsigned char)c)
			return (strc);
		strc++;
	}
	return (0);
}
